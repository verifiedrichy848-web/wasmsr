<div class="page-title">Author Guidelines (Including Peer Review Outcome)</div>

<div class="card" style="max-width:900px; margin:auto; line-height:1.8; padding:30px;">

    <h3>Manuscript Preparation</h3>
    <p>Manuscripts must be written in clear, standard academic English and follow APA style for in-text citations and references. Articles should be structured in a way that fits their type, typically including: abstract, introduction, literature review, methodology, results, discussion, and conclusion, with additional sections (for example, implications, limitations, recommendations) where appropriate.</p>

    <p>The journal publishes original empirical, conceptual, and theory-based work that makes a distinct contribution to knowledge and practice. Authors should use appropriate quantitative, qualitative, or mixed methods designs and describe variables, instruments, and analytical procedures in sufficient detail to allow replication and critical appraisal.</p>

    <p>Empirical manuscripts should normally include: abstract; introduction and rationale; clearly stated objectives; research questions and/or hypotheses; conceptual or theoretical framework; detailed methodology (study design, participants or data sources, instruments, procedures, and analysis); results; discussion; and conclusion. Conceptual and qualitative studies should follow a coherent methodological approach, specifying research design, data sources, analytical strategy, and criteria for evaluating interpretive claims. Mixed methods papers should report each component transparently and justify the use of triangulation.</p>

    <h3>Submission Guidelines</h3>
    <p>Full-length articles should normally be 5,000–8,000 words (excluding references, tables, and appendices). Short reports, book reviews, and grant or project notes should not exceed 3,000 words. Each manuscript must include an abstract of 150–250 words and 5–6 keywords. Manuscripts should be prepared in 12 point Times New Roman, with 1.5 line spacing and standard margins, and submitted in an editable word processing format (for example, Microsoft Word). Authors must ensure that every in-text citation has a corresponding, complete reference entry.</p>

    <h3>Duration and Outcome of Peer Review</h3>
    <p>The journal aims to provide an initial editorial decision within six (6) months of submission. Authors may track the status of their manuscripts through the online submission and tracking system. The corresponding author will be notified by email and through the system whether the manuscript is: accepted for publication, accepted subject to minor revisions, invited for major revisions and resubmission, or rejected. Where necessary, authors may appeal editorial decisions following the procedures described on the journal website.</p>

