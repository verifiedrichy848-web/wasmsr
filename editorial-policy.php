    <p>The West African Social and Management Sciences Review (WASMSR) operates a rigorous <strong>double-blind peer review process</strong>. All eligible submissions are evaluated by at least two independent reviewers based on:</p>

    <ul>
        <li>Originality of the research</li>
        <li>Methodological soundness</li>
        <li>Clarity of presentation</li>
        <li>Ethical compliance</li>
        <li>Contribution to existing knowledge</li>
    </ul>

    <p>Editorial decisions are made by the <strong>Editor-in-Chief</strong>, supported by the Editorial Board, and are <strong>not influenced</strong> by the authors’ institutional affiliation, nationality, gender, or funding source.</p>

    <p>The journal follows recognized international guidelines on good editorial practice and publication ethics. WASMSR ensures:</p>

    <ul>
        <li>A fair and timely review process</li>
        <li>Opportunities for authors to respond to reviewer comments</li>
        <li>Transparent procedures for appeals and complaints</li>
    </ul>

    <p>By maintaining high editorial standards, WASMSR ensures the publication of rigorous, ethical, and impactful research that contributes to scholarship and practice in social and management sciences.</p>

